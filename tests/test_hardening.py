def test_cli_missing_file_exits_2(capsys):
    rc = main(["generate", "/no/such/file/intent.yaml"])
    assert rc == 2
    err = capsys.readouterr().err
    assert "error" in err.lower()


def test_cli_missing_file_json_format(capsys):
    rc = main(["--format", "json", "generate", "/no/such/file.yaml"])
    assert rc == 2
    payload = json.loads(capsys.readouterr().out)
    assert payload["ok"] is False
    assert "error" in payload


def test_cli_directory_as_input_exits_2(tmp_path, capsys):
    """Passing a directory path instead of a file must exit 2 cleanly."""
    rc = main(["generate", str(tmp_path)])
    assert rc == 2
    err = capsys.readouterr().err
    assert "error" in err.lower()


def test_cli_malformed_yaml_exits_2(tmp_path, capsys):
    p = tmp_path / "bad.yaml"
    p.write_text("target: x\nplatform\thooks: []\n", encoding="utf-8")
    rc = main(["generate", str(p)])
    assert rc == 2
    err = capsys.readouterr().err
    assert "error" in err.lower()


def test_cli_empty_yaml_exits_2(tmp_path, capsys):
    """An empty YAML file (parses to None) must exit 2 with a clear message."""
    p = tmp_path / "empty.yaml"
    p.write_text("# nothing here\n", encoding="utf-8")
    rc = main(["generate", str(p)])
    assert rc == 2
    err = capsys.readouterr().err
    assert "error" in err.lower()


def test_cli_lint_missing_file_exits_2(capsys):
    rc = main(["lint", "/no/such/file.yaml"])
    assert rc == 2
    err = capsys.readouterr().err
    assert "error" in err.lower()


