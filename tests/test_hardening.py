def test_cli_generate_output_to_directory_exits_2(tmp_path, capsys):
    """Writing the script to a directory path (not a file) must exit 2."""
    demo = os.path.join(ROOT, "demos", "01-basic", "intent.yaml")
    rc = main(["generate", demo, "-o", str(tmp_path)])
    assert rc == 2
    err = capsys.readouterr().err
    assert "error" in err.lower()


